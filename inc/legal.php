function cars_get_legal_page_url($key)
{
    $pages = cars_get_legal_pages();

    if (empty($pages[$key])) {
        return home_url("/");
    }

    $page = get_page_by_path($pages[$key]["slug"]);

    return $page ? get_permalink($page) : home_url("/" . $pages[$key]["slug"] . "/");
}

function cars_is_legal_page($post = null)
{
    $post = $post ?: get_post();

    if (!$post || $post->post_type !== "page") {
        return false;
    }

    $slugs = array_map(static function ($page) {
        return $page["slug"];
    }, cars_get_legal_pages());

    return in_array($post->post_name, $slugs, true);
}

add_action("init", static function () {
    foreach (cars_get_legal_pages() as $page_config) {
        $page = get_page_by_path($page_config["slug"]);

        if (!$page) {
            $page_id = wp_insert_post([
                "post_type" => "page",
                "post_status" => "publish",
                "post_title" => $page_config["title"],
                "post_name" => $page_config["slug"],
                "post_content" => trim($page_config["content"]),
                "comment_status" => "closed",
                "ping_status" => "closed",
            ]);
        } else {
            $page_id = (int) $page->ID;
        }

        if (!empty($page_id) && !is_wp_error($page_id)) {
            update_post_meta($page_id, "_wp_page_template", "page-legal.php");
        }
    }

    $legacy_consent_page = get_page_by_path(
        "consent-to-personal-data-processing",
    );

    if (
        $legacy_consent_page &&
        $legacy_consent_page->post_status !== "trash"
    ) {
        wp_trash_post($legacy_consent_page->ID);
    }
});

add_filter("template_include", static function ($template) {
    if (is_page() && cars_is_legal_page()) {
        return get_template_directory() . "/page-legal.php";
    }

    return $template;
});

add_filter("body_class", static function ($classes) {
    if (is_page() && cars_is_legal_page()) {
        $classes[] = "legal-page-body";
    }

    return $classes;
});
